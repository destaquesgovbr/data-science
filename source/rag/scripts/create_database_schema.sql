-- ===================================
-- Full-Text Search Setup
-- ===================================
-- Add tsvector column for Portuguese full-text search

ALTER TABLE document_chunks ADD COLUMN IF NOT EXISTS tsv tsvector;

-- Create GIN index for full-text search
CREATE INDEX IF NOT EXISTS idx_chunks_fts ON document_chunks USING GIN(tsv);

-- Create trigger to auto-update tsvector
CREATE OR REPLACE FUNCTION chunks_tsv_trigger() RETURNS trigger AS $$
BEGIN
  NEW.tsv := to_tsvector('portuguese', COALESCE(NEW.content, ''));
  RETURN NEW;
END
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS tsvectorupdate ON document_chunks;
CREATE TRIGGER tsvectorupdate BEFORE INSERT OR UPDATE ON document_chunks
  FOR EACH ROW EXECUTE FUNCTION chunks_tsv_trigger();

COMMENT ON COLUMN document_chunks.tsv IS 'Automatically generated tsvector for Portuguese full-text search';

