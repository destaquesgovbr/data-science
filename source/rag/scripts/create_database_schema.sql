-- Enable pgvector extension
CREATE EXTENSION IF NOT EXISTS vector;

-- Drop existing tables (use with caution in production!)
DROP TABLE IF EXISTS document_chunks CASCADE;
DROP TABLE IF EXISTS news_documents CASCADE;

-- ===================================
-- Table: news_documents
-- ===================================
-- Stores document metadata and full content

CREATE TABLE news_documents (
    id SERIAL PRIMARY KEY,
    title TEXT NOT NULL,
    content TEXT NOT NULL,
    url TEXT UNIQUE,  -- IMPORTANT: unique constraint for ON CONFLICT
    source_agency TEXT,
    category TEXT,
    published_at TIMESTAMP,
    updated_at TIMESTAMP DEFAULT NOW(),
    metadata JSONB,
    indexed_at TIMESTAMP DEFAULT NOW()
);

COMMENT ON TABLE news_documents IS 'Stores metadata and full content of news documents';
COMMENT ON COLUMN news_documents.url IS 'Unique URL - used for deduplication in indexing';
COMMENT ON COLUMN news_documents.metadata IS 'Additional metadata in JSON format (tags, etc)';

-- ===================================
-- Table: document_chunks
-- ===================================
-- Stores text chunks with vector embeddings

CREATE TABLE document_chunks (
    id SERIAL PRIMARY KEY,
    document_id INTEGER NOT NULL REFERENCES news_documents(id) ON DELETE CASCADE,
    chunk_index INTEGER NOT NULL,
    content TEXT NOT NULL,
    enriched_content TEXT,  -- Optional: LLM-enriched version
    embedding vector(1024),  -- BGE-M3 embeddings (1024 dimensions)
    chunk_type TEXT,  -- semantic, fixed, sentence, etc
    char_start INTEGER,  -- Start position in original document
    char_end INTEGER,  -- End position in original document
    tokens INTEGER,  -- Token count
    created_at TIMESTAMP DEFAULT NOW(),
    UNIQUE(document_id, chunk_index)
);

COMMENT ON TABLE document_chunks IS 'Text chunks with vector embeddings for semantic search';
COMMENT ON COLUMN document_chunks.embedding IS 'Vector embedding (1024d) generated by BGE-M3';
COMMENT ON COLUMN document_chunks.enriched_content IS 'Optional LLM-enriched version for better retrieval';

-- ===================================
-- Indexes - Basic
-- ===================================

-- Foreign key index
CREATE INDEX idx_chunks_document_id ON document_chunks(document_id);

-- Category filtering
CREATE INDEX idx_documents_category ON news_documents(category);

-- URL lookup
CREATE INDEX idx_documents_url ON news_documents(url);

-- Date range queries
CREATE INDEX idx_documents_published ON news_documents(published_at DESC);

-- Source agency filtering
CREATE INDEX idx_documents_agency ON news_documents(source_agency);

